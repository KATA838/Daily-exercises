# 第三次：vlan = 30
# ...
# 第五次：vlan = 50
# 列表有几个元素，就循环几次
for vlan in vlans:
    
    # 每次打印当前正在配置的VLAN
    print(f"正在配置vlan {vlan}")
    
# 循环结束后，所有VLAN都"配置"完了