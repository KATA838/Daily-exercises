# 功能: for循环基础，打印数字1-5
# ============================================

# for 是循环关键字，意思是"对于...中的每一个"
# i 是变量名，每次循环取一个值（可以叫任何名字，i是习惯）
# in 表示"在...里面"
# range(1, 6) 生成一个数字序列：1, 2, 3, 4, 5
# 注意：range(1, 6) 从1开始，到6结束（不包括6），所以是1-5
for i in range(1,6):
    
    # 每次循环执行这里的代码
    # f"当前数字{i}" 会把 {i} 替换成当前数字
    # 第一次：i=1，输出"当前数字1"
    # 第二次：i=2，输出"当前数字2"
    # ...
    print(f"当前数字{i}")
    
# 循环结束后，程序继续往下执行（这个文件没有后续代码了）