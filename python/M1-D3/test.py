# ---------- 年龄判断 ----------
# input() 获取用户输入，int() 直接转成整数
# 可以写成一行，因为 input() 的结果直接传给 int()
age = int(input("年龄是?\n"))

# 判断年龄
if age < 18:
    # 小于18岁，未成年
    print(f"未成年，年龄:{age}")
else:
    # 大于等于18岁，成年
    print(f"成年，年龄:{age}")

# ---------- 成绩判断 ----------
# 同样的方式获取成绩
score = int(input("成绩？\n"))

# 多条件判断
if score >= 90 :
    # 90分及以上：优秀
    print(f"优秀，成绩:{score}")
elif score >= 60:
    # 60-89分：及格
    # 注意：能走到这里，说明 score < 90（因为上面的条件不满足）
    print(f"及格,成绩:{score}")
else:
    # 60分以下：不及格
    print(f"不及格，成绩:{score}")